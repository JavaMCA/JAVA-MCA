class UnaryOperator{
    public static void main(String args[]){
        int a=10;
        boolean b=true;
        System.out.println("a++=" +a++);
        System.out.println("++a=" +(++a));
        System.out.println("a--=" +a--);
        System.out.println("--a=" +(--a));
        System.out.println("!b=" +(!b));
    }
}